# Define variables
principal = 1000     # P: Principal amount in currency units (e.g., dollars)
rate = 0.05          # R: Annual interest rate as a decimal (5%)
time = 3             # T: Time in years

# Calculate simple interest
interest = principal * rate * time

# Print the result
print(f"Simple interest on an investment of {principal} at an annual rate of {rate*100}% for {time} years is {interest}")
